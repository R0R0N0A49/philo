#include "philo.h"

void    ft_free_tab(void **tab)
{
    int i;

    if (tab && tab[0])
    {
        i = 0;
        while (tab[i])
        {
            free(tab[i]);
            i++;
        }
    }
    if (tab)
        free(tab);
}
